package com.example.unidad5_ej8;

public class Datos {
    private int imagen;
    private String texto;

    public Datos(int imagen, String texto) {
        this.imagen = imagen;
        this.texto = texto;
    }

    public int getImagen() {
        return imagen;
    }

    public String getTexto() {
        return texto;
    }
}
